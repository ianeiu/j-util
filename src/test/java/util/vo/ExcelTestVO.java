package util.vo;

public class ExcelTestVO {
	private String abc;
	private String cde; 
	private String efg;
	private String ghi;
	private String ijk;
	public String getAbc() {
		return abc;
	}
	public void setAbc(String abc) {
		this.abc = abc;
	}
	public String getCde() {
		return cde;
	}
	public void setCde(String cde) {
		this.cde = cde;
	}
	public String getEfg() {
		return efg;
	}
	public void setEfg(String efg) {
		this.efg = efg;
	}
	public String getGhi() {
		return ghi;
	}
	public void setGhi(String ghi) {
		this.ghi = ghi;
	}
	public String getIjk() {
		return ijk;
	}
	public void setIjk(String ijk) {
		this.ijk = ijk;
	}
	@Override
	public String toString() {
		return "ExcelTestVO [abc=" + abc + ", cde=" + cde + ", efg=" + efg
				+ ", ghi=" + ghi + ", ijk=" + ijk + "]";
	}
	
	
}
